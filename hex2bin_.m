%converting a h character hex message to a  k-bit binary message

function[x]=hex2bin_(c,k,h)
x=zeros(1,k);
for i=1:h
    y=c(i);
   
    if (y=='0')
        x(1+4*i-4)=0;
        x(2+4*i-4)=0;
        x(3+4*i-4)=0;
        x(4+4*i-4)=0;
    end
    if (y=='1')
        x(1+4*i-4)=0;
        x(2+4*i-4)=0;
        x(3+4*i-4)=0;
        x(4+4*i-4)=1;
    end
    if (y=='2')
        x(1+4*i-4)=0;
        x(2+4*i-4)=0;
        x(3+4*i-4)=1;
        x(4+4*i-4)=0;
    end
    if (y=='3')
        x(1+4*i-4)=0;
        x(2+4*i-4)=0;
        x(3+4*i-4)=1;
        x(4+4*i-4)=1;
    end
    if (y=='4')
        x(1+4*i-4)=0;
        x(2+4*i-4)=1;
        x(3+4*i-4)=0;
        x(4+4*i-4)=0;
    end
    if (y=='5')
        x(1+4*i-4)=0;
        x(2+4*i-4)=1;
        x(3+4*i-4)=0;
        x(4+4*i-4)=1;
    end
    if (y=='6')
        x(1+4*i-4)=0;
        x(2+4*i-4)=1;
        x(3+4*i-4)=1;
        x(4+4*i-4)=0;
    end
    if (y=='7')
        x(1+4*i-4)=0;
        x(2+4*i-4)=1;
        x(3+4*i-4)=1;
        x(4+4*i-4)=1;
    end
    if (y=='8')
        x(1+4*i-4)=1;
        x(2+4*i-4)=0;
        x(3+4*i-4)=0;
        x(4+4*i-4)=0;
    end
    if (y=='9')
        x(1+4*i-4)=1;
        x(2+4*i-4)=0;
        x(3+4*i-4)=0;
        x(4+4*i-4)=1;
    end
    
    if (y=='A' || y=='a')
        x(1+4*i-4)=1;
        x(2+4*i-4)=0;
        x(3+4*i-4)=1;
        x(4+4*i-4)=0;
    end
    if (y=='B' || y=='b')
        x(1+4*i-4)=1;
        x(2+4*i-4)=0;
        x(3+4*i-4)=1;
        x(4+4*i-4)=1;
    end
    if (y=='C' || y=='c')
        x(1+4*i-4)=1;
        x(2+4*i-4)=1;
        x(3+4*i-4)=0;
        x(4+4*i-4)=0;
    end
    if (y=='D' || y=='d')
        x(1+4*i-4)=1;
        x(2+4*i-4)=1;
        x(3+4*i-4)=0;
        x(4+4*i-4)=1;
    end
    if (y=='E' || y=='e')
        x(1+4*i-4)=1;
        x(2+4*i-4)=1;
        x(3+4*i-4)=1;
        x(4+4*i-4)=0;
    end
    if (y=='F' || y=='f')
        x(1+4*i-4)=1;
        x(2+4*i-4)=1;
        x(3+4*i-4)=1;
        x(4+4*i-4)=1;
    end
 
end

end